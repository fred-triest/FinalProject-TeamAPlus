    public void populateNetworkComboBox() {
        
        // Remove default values and potential duplicates
        cmbNetwork.removeAllItems();
        
        // Add each network to combo box
        for (Network network : system.getNetworkList()) {
            cmbNetwork.addItem(network.getName());
        }
    }
    
    public void populateTable() {
        
        // Get table model and clear existing rows
        DefaultTableModel model = (DefaultTableModel) tblManageEnterprises.getModel();
        model.setRowCount(0);
        
        // Loop through networks and enterprises to populate table
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = enterprise.getOrganizationId();
                row[1] = enterprise.getName();
                row[2] = network.getName();
                model.addRow(row);
            }
        }
    }

