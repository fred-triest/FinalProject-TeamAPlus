    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        // Get selected network name and find matching Network object
        String selectedNetworkName = (String) cmbNetwork.getSelectedItem();
        Network selectedNetwork = null;
        for (Network network : system.getNetworkList()) {
            if (network.getName().equals(selectedNetworkName)) {
                selectedNetwork = network;
                break;
            }
        }
        
        // Get enterprise name from text field
        String enterpriseName = txtName.getText().trim();
        
        // Make sure name field is not null
        if (enterpriseName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter an enterprise name", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Create new enterprise and add to selected network
        selectedNetwork.getEnterpriseDirectory().addEnterprise(new GenericEnterprise(enterpriseName));
        
        // Refresh table and clear the name field
        populateTable();
        JOptionPane.showMessageDialog(null, "Enterprise added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        txtName.setText("");
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        // Get selected row from the table
        int selectedRow = tblManageEnterprises.getSelectedRow();
        
        // Check to see if a row is selected
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select an enterprise to delete", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Get the enterprise ID from the selected row
        String enterpriseId = (String) tblManageEnterprises.getValueAt(selectedRow, 0);
        
        // Find and remove the enterprise from the correct network
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise.getOrganizationId().equals(enterpriseId)) {
                    network.getEnterpriseDirectory().removeEnterprise(enterprise);
                    JOptionPane.showMessageDialog(null, "Enterprise deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    populateTable();
                    return;
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


